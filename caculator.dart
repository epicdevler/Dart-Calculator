class Calculator {
  int add(int valueOne, int valueTwo) {
    var result = valueOne + valueTwo;
    return result;
  }

  int substract(int valueOne, int valueTwo) {
    return valueOne - valueTwo;
  }

  int divide(int valueOne, int valueTwo) {
    return valueOne ~/ valueTwo;
  }

  int multiply(int valueOne, int valueTwo) {
    return valueOne * valueTwo;
  }
}
